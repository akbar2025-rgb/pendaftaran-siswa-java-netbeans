    private void editSiswaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editSiswaActionPerformed
        // TODO add your handling code here:
        if(namaSiswa.getText().length() < 3)
        {
            JOptionPane.showMessageDialog(this,"<html><p1 style='font-family: Bahnschrift; font-size: 18pt;'><b>Please insert the proper id!</b>");
        }
        else if(idSiswa.getText().isEmpty() || namaSiswa.getText().isEmpty() ||  kelamin.getSelectedItem().equals("None") || ttl.getText().isEmpty() || namaAyah.getText().isEmpty() || namaIbu.getText().isEmpty() || alamat.getText().isEmpty() || asalSekolah.getText().isEmpty() || noHp.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this,"<html><p1 style='font-family: Bahnschrift; font-size: 18pt;'><b>Please insert the complete info!</b>");
        }
        else
        {
            try
            {
                int row = jTable1.getSelectedRow();
                if(jTable1.getSelectedRow() == -1)
                {
                    JOptionPane.showMessageDialog(null,"<html><p1 style='font-family: Bahnschrift; font-size: 18pt;'><b>Record is not selected in the table!</b>");
                    idSiswa.setText("");
                    namaSiswa.setText("");
                    kelamin.setSelectedItem("None");
                    ttl.setText("");
                    namaAyah.setText("");
                    namaIbu.setText("");
                    alamat.setText("");
                    asalSekolah.setText("");
                    kelamin.setSelectedItem("None");
                    noHp.setText("");
                    idSiswa.requestFocus();
                }
                else
                {
                    System.out.println("Connecting to database...");
                    Class.forName("com.mysql.jdbc.Driver");
                    con = DriverManager.getConnection("jdbc:mysql://localhost:3306/tpidarulhikmah", "root", "");
                    String value = jTable1.getModel().getValueAt(row, 0).toString();
                    preState = con.prepareStatement("update siswa SET nama_siswa = ?, kelamin = ?, ttl = ?, nama_ayah = ?, nama_ibu = ?, alamat = ?, asal_sekolah = ?, no_hp = ? WHERE id_siswa ="+value);
                    preState.setString(1,namaSiswa.getText());
                    preState.setString(2,kelamin.getSelectedItem().toString());
                    preState.setString(3,ttl.getText());
                    preState.setString(4,namaAyah.getText());
                    preState.setString(5,namaIbu.getText());
                    preState.setString(6,alamat.getText());
                    preState.setString(7,asalSekolah.getText());
                    preState.setString(8,noHp.getText());
                    preState.executeUpdate();
                    DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
                    model.setRowCount(0);
                    showTable();
                    JOptionPane.showMessageDialog(null,"<html><p1 style='font-family: Bahnschrift; font-size: 18pt;'><b>Student record has been updated successfully!</b>");
                    idSiswa.setText("");
                    namaSiswa.setText("");
                    kelamin.setSelectedItem("None");
                    ttl.setText("");
                    namaAyah.setText("");
                    namaIbu.setText("");
                    alamat.setText("");
                    asalSekolah.setText("");
                    kelamin.setSelectedItem("None");
                    noHp.setText("");
                    idSiswa.requestFocus();
                }            
            }
            catch (SQLException ex) 
            {
                JOptionPane.showMessageDialog(null,"<html><p1 style='font-family: Bahnschrift; font-size: 18pt;'><b>Database connectivity failure!</b>");
            }
            catch (ClassNotFoundException ex) 
            {
                JOptionPane.showMessageDialog(null,"<html><p1 style='font-family: Bahnschrift; font-size: 18pt;'><b>Class not found!</b>");
            }
        }
    }//GEN-LAST:event_editSiswaActionPerformed

