    private void namaSiswaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_namaSiswaKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if(!(Character.isLetter(c) || c == java.awt.event.KeyEvent.VK_BACK_SPACE || c == '-' || c == java.awt.event.KeyEvent.VK_SPACE || c == java.awt.event.KeyEvent.VK_DELETE))
        {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_namaSiswaKeyTyped

